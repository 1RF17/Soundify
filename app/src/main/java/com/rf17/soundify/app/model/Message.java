package com.rf17.soundify.app.model;

public class Message {
    private String mText1;
    private String mText2;

    public Message (String text1, String text2){
        mText1 = text1;
        mText2 = text2;
    }

    public String getmText1() {
        return mText1;
    }

    public String getmText2() {
        return mText2;
    }

}
